// State for all of the liquid effects
static LiquidState gWaterState;
static LiquidState gSlimeState;
static LiquidState gBloodState;
static LiquidState gLavaState;

//----------------------------------------------------------------------------------------------------------------------
// Initialize the state for a single liquid
//----------------------------------------------------------------------------------------------------------------------
static void InitLiquidAnim(LiquidState& state) noexcept {
    state.rngIndex = 0;
    state.pos = {};
    state.vel = { 2, 1, 3, 4 };
}

//----------------------------------------------------------------------------------------------------------------------
// Reset all liquid effects back to their initial state
//----------------------------------------------------------------------------------------------------------------------
void P_InitLiquidAnims() noexcept {
    InitLiquidAnim(gWaterState);
    InitLiquidAnim(gSlimeState);
    InitLiquidAnim(gBloodState);
    InitLiquidAnim(gLavaState);
}

//----------------------------------------------------------------------------------------------------------------------
// Update the animation for a type of liquid.
// Takes the animation structure, the liquid state and the mapping from effect intensity to color index.
//----------------------------------------------------------------------------------------------------------------------
static void UpdateLiquid(const anim_t& anim, LiquidState& liquidState, const uint8_t liquidIntensityLUT[256]) noexcept {
    // Sanity check the animation is valid flat animation
    if ((anim.current >= gNumFlatLumps) || anim.istexture)
        return;

    // Get the texture to be updated and the lump data for the texture.
    // Abort if the texture is not valid to be updated as a liquid:
    const texture_t& baseTex = gpFlatTextures[anim.basepic];
    texture_t& tex = gpFlatTextures[anim.current];
    const WadLump& texLump = W_GetLump(tex.lumpNum);
    
    const bool bCanDoLiquidFx = (
        baseTex.bIsCached &&                    // Base texture in the animation must have a place in VRAM
        (texLump.pCachedData != nullptr) &&     // Data must be loaded for the texture
        texLump.bIsUncompressed &&              // Data that we will overwrite must be raw/uncompressed
        (tex.width == 64) &&                    // Size must be exactly 64x64
        (tex.height == 64) &&
        (texLump.uncompressedSize >= 64u * 64u + sizeof(texlump_header_t))  // Sanity check there is enough data
    );
    
    if (!bCanDoLiquidFx)
        return;
        
    uint8_t* pTexData = (uint8_t*) texLump.pCachedData + sizeof(texlump_header_t);
  
    // Update the liquid
    const uint8_t curRngIndex = liquidState.rngIndex;
    XYVectors curPos = liquidState.pos;

    for (uint32_t y = 0; y < 64; ++y) {
        curPos.x1 = liquidState.pos.x1;
        curPos.x2 = liquidState.pos.x2;
  
        for (uint32_t x = 0; x < 64; ++x) {
            const uint8_t intensity = (uint8_t)(
                curRngIndex +
                (uint8_t) SINE_WAVE[curPos.x1] +
                (uint8_t) SINE_WAVE[curPos.x2] +
                (uint8_t) SINE_WAVE[curPos.y1] +
                (uint8_t) SINE_WAVE[curPos.y2]
            );
            
            const uint8_t colorIndex = liquidIntensityLUT[intensity];
            *pTexData++ = colorIndex;

            curPos.x1 = (uint8_t)(curPos.x1 + 4);
            curPos.x2 = (uint8_t)(curPos.x2 + 8);
        }

        curPos.y1 = (uint8_t)(curPos.y1 + 4);
        curPos.y2 = (uint8_t)(curPos.y2 + 8);
    }
    
    // Update the velocity vector for the effect
    const uint8_t nextRngIndex = (uint8_t)(curRngIndex + 1);
    const uint8_t randomNum = gRndTable[nextRngIndex];
    const int8_t curVel = (int8_t)(uint8_t) liquidState.vel[randomNum];
    
    if (nextRngIndex & 2) {
        liquidState.vel[randomNum] = (uint8_t)(int8_t) std::min((int) curVel + 1, +9);
    } else {
        liquidState.vel[randomNum] = (uint8_t)(int8_t) std::max((int) curVel - 1, -9);
    }
    
    // Update the rng index and position vectors for the effect (based on velocity)
    liquidState.rngIndex = nextRngIndex;
    liquidState.pos.x1 = (uint8_t)(liquidState.pos.x1 + liquidState.vel.x1);
    liquidState.pos.x2 = (uint8_t)(liquidState.pos.x2 + liquidState.vel.x2);
    liquidState.pos.y1 = (uint8_t)(liquidState.pos.y1 + liquidState.vel.y1);
    liquidState.pos.y2 = (uint8_t)(liquidState.pos.y2 + liquidState.vel.y2);
    
    // Mark the texture as needing a re-upload
    tex.uploadFrameNum = TEX_INVALID_UPLOAD_FRAME_NUM;
}

//----------------------------------------------------------------------------------------------------------------------
// Update the animations for the different types of liquids
//----------------------------------------------------------------------------------------------------------------------
void P_AnimLiquid_Water(const anim_t& anim) noexcept {
    UpdateLiquid(anim, gWaterState, WATER_INTENSITY_LUT);
}

void P_AnimLiquid_Slime(const anim_t& anim) noexcept {
    UpdateLiquid(anim, gSlimeState, SLIME_INTENSITY_LUT);
}

void P_AnimLiquid_Blood(const anim_t& anim) noexcept {
    UpdateLiquid(anim, gBloodState, BLOOD_INTENSITY_LUT);
}

void P_AnimLiquid_Lava(const anim_t& anim) noexcept {
    UpdateLiquid(anim, gLavaState, LAVA_INTENSITY_LUT);
}

