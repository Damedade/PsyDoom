// Holds two unsigned 8-bit vectors in the x and y directions.
// Used to offset where to sample from in the effect's waveform.
struct XYVectors {
    uint8_t x1, x2;
    uint8_t y1, y2;
    
    // Indexing operators with wraparound
    uint8_t& operator [](const size_t idx) {
        switch (idx & 3) {
            case 0: return x1;
            case 1: return x2;
            case 2: return y1;
            case 3: return y2;
        }
    
        return x1;
    }

    uint8_t operator [](const size_t idx) const noexcept {
        switch (idx & 3) {
            case 0: return x1;
            case 1: return x2;
            case 2: return y1;
            case 3: return y2;
        }
    
        return x1;
    }
};

